package africa.semicolon.RideHailing;

public class Profile {

    private String bio;
    private String sex;
    private String name;
    private String username;


    public void setbio(String Profilebio) {
        bio = Profilebio;
    }
    public String getbio() {
        return bio;
    }


    public void setsex(String Usersex) {
        sex = Usersex;
    }
    public String getsex() {
        return sex;
    }


    public void setname(String Profilename) {
        name  = Profilename;
    }
    public String getname() {
        return name;
    }


    public void setusername(String ProfileUsername) {
        username = ProfileUsername;
    }  public String getusername() {
        return username;
    }
}

